    ETL_TOKKI_SENSOR_LOG = "ETL_TOKKI_SENSOR_LOG"
    ETL_TOKKI_STATE_SECTION_LOG = "ETL_TOKKI_STATE_SECTION_LOG"
    ETL_TOKKI_STATE_SECTION_SPAN = "ETL_TOKKI_STATE_SECTION_SPAN"
    ETL_TOKKI_EVENT_SECTION_LOG = "ETL_TOKKI_EVENT_SECTION_LOG"
