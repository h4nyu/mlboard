    TOKKI_EVENT_LOG = "TOKKI_EVENT_LOG"
    TOKKI_PROCESS_LOG = "TOKKI_PROCESS_LOG"
    TOKKI_SENSOR_LOG = "TOKKI_SENSOR_LOG"


